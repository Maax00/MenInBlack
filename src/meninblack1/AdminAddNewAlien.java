    private void fillCBLocation() {
        selectLocation.addItem("");
        selectLocation.removeItem("Item 1");
        selectLocation.removeItem("Item 2");
        selectLocation.removeItem("Item 3");
        selectLocation.removeItem("Item 4");
        try {
              // Fetch the list of locations
            ArrayList<String> locations = Database.getDatabase().fetchColumn("select Benamning from plats");
            // Populate the combo box with locations
            for (String oneLocation : locations) {
                selectLocation.addItem(oneLocation);
            }
        } catch (InfException ex) {
            System.out.println(ex);
        }
    }
    // This method is used to set the Alien_ID in an entry field
    private void setID(){
        try{
             // Query the database to get the next Auto Increment value for Alien_ID in the 'alien' table
            String count = Database.getDatabase().getAutoIncrement("alien", "Alien_ID");
             // Set the obtained Auto Increment value to the entryAlienID field
            entryAlienID.setText(count);
        }catch(InfException ex){
            System.out.println(ex);
        }
    }
