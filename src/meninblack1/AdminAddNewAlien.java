    private void registerAlienButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerAlienButtonActionPerformed
        // TODO add your handling code here:
        boolean check = true;
        try {
            int alienID = Integer.parseInt(entryAlienID.getText());
            String date = entryDate.getText();
            if (!Validering.isDate(date)) {
                check = false;
                messageBox.setText("Date Error");
            }
            String email = entryEmail.getText();
            if (!Validering.isValidEmail(email)) {
                check = false;
                messageBox.setText("Email Error");
            }
            String location = selectLocation.getSelectedItem().toString();

            String name = entryName.getText();
            if (!Validering.isNameLetters(name)) {
                check = false;
                messageBox.setText("Name Error");
            }
            String password = entryPassword.getText();
            if (!Validering.isValidPassword(password)) {
                check = false;
                messageBox.setText("Password Error");
            }
            String phonenumber = entryPhonenumber.getText();
            if (!Validering.isValidPhone(phonenumber)) {
                check = false;
                messageBox.setText("Phonenumber Error");
            }
            if (check) {
                messageBox.setText("Complete");
                // Build a SQL query to get the Agent_ID of the responsible agent based on their name
                String getNameQuery = "select Agent_ID from agent where Namn = '" + selectCBAgent.getSelectedItem().toString() + "'";
                int responsibleAgent = Integer.parseInt(Database.getDatabase().fetchSingle(getNameQuery));
                 // Build a SQL query to fetch the Plats_ID of the location from the 'plats' table
                String fetchLocationIDQuery = "select Plats_ID from plats where Benamning ='" + location + "'";
                int locationID = Integer.parseInt(Database.getDatabase().fetchSingle(fetchLocationIDQuery));
                // Build the SQL query to insert alien information into the 'alien' table
                String insertQuery = "insert into alien " + "values (" + alienID + ",'" + date + "','" + email + "','" + password + "','" + name + "','" + phonenumber + "'," + locationID + ", " + responsibleAgent + ")";
                 // Depending on the selected race, open a new frame for further registration
                switch (selectCB.getSelectedItem().toString().toLowerCase()) {
                    case "squid":
                        RegisterNewRaceSquid frame = new RegisterNewRaceSquid();
                        frame.setVisible(true);
                        break;
                    case "boglodite":
                        RegisterNewRaceBoglodite frameB = new RegisterNewRaceBoglodite();
                        frameB.setVisible(true);
                        break;
                    case "worm":
                        RegisterNewRaceWorm frameW = new RegisterNewRaceWorm();
                        frameW.setVisible(true);
                        break;
                    default:
                        break;
                }
                // Execute the insert query to add alien information to the database
                Database.getDatabase().insert(insertQuery);
                
            }
        } catch (InfException ex) {
            System.out.println("Fel med " + ex);
            messageBox.setText("Insert valid data");
        }
    }//GEN-LAST:event_registerAlienButtonActionPerformed

